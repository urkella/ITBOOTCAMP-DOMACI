// Zbir, razlika, proizvod i kolicnik
var zbir;
var razlika;
var proizvod;
var kolicnik;

// Definisati brojeve
var prviBroj = 7;
var drugiBroj = 3;

// Kalkulacija
zbir = prviBroj + drugiBroj; // zbir brojeva
razlika = prviBroj - drugiBroj; // razlika brojeva
proizvod = prviBroj * drugiBroj; // proizvod brojeva
kolicnik = prviBroj / drugiBroj; // kolicnik brojeva
ostatak = prviBroj % drugiBroj; // ostatak pri deljenju
stepen = prviBroj ^ drugiBroj; // stepen

// Ispisati rezultate
console.log("Zbir: " + prviBroj + " + " + drugiBroj + " = " + zbir);
console.log("Razlika: " + prviBroj + " - " + drugiBroj + " = " + razlika);
console.log("Proizvod: " + prviBroj + " * " + drugiBroj + " = " + proizvod);
console.log("Kolicnik: " + prviBroj + " / " + drugiBroj + " = " + kolicnik);
console.log(
  "Ostatak pri deljenju: " + prviBroj + " % " + drugiBroj + " = " + ostatak
);
console.log("Stepen: " + prviBroj + " ^ " + drugiBroj + " = " + stepen);
